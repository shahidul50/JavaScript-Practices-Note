// Assignment Operators
let number;    // Variable Declaration.
number = 100;   //Value Assign. (=) Assignment Operator Assigns a Value in Variable.
console.log(number);

// += Operator.

let sum = 10;
 sum += 10;

 console.log(sum);

// -= Operator.
let sub = 50;
 sub -= 10;

 console.log(sub);

// *= Operator.
let mul = 10;
 mul *= 10;

 console.log(mul);

// /= Operator.
let div = 10;
 div /= 10;

 console.log(div);

// **= Operator.
let exp = 10;
 exp **= 5;

 console.log(exp);

// %= Operator.
let mod = 12;
 mod %= 10;

 console.log(mod);