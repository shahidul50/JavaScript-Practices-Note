// Comparison Operators
//equal to comparison operator:
let a;
a = 8 == 8;
console.log(a);

let b;
b = 5 == 8;
console.log(b);

let c;
c = '80' == 80;
console.log(c);

//equal to and equal type comparison operator:
let d;
d = '80' === 80;
console.log(d);

//not equal comparison operator:
let e;
e = 80 != 80;
console.log(e);

let f;
f = 8 != 80;
console.log(f);

//not equal value or not equal type comparison operator:
let g;
g = '80' !== 80;
console.log(g);

//greater than comparison operator:
let num1= 20;
let num2 = 30;
let result1 = num1 > num2;
console.log(result1);

//less than comparison operator:
let num3= 20;
let num4 = 30;
let result2 = num3 < num4;
console.log(result2);

//greater than or equal to comparison operator:
let num5= 29;
let num6 = 30;
let result3 = num5 >= num6;
console.log(result3);

//less than or equal to comparison operator:
let num7= 30;
let num8 = 30;
let result4 = num7 <= num8;
console.log(result4);
